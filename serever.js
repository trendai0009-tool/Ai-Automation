// ========== HELPER: Get or Create User ==========
async function getOrCreateUser(supabaseId, email, name) {
  let { data: user, error } = await supabaseAdmin
    .from('users')
    .select('*')
    .eq('supabase_id', supabaseId)
    .single();
  
  if (error && error.code !== 'PGRST116') {
    console.error('Error fetching user:', error);
  }
  
  if (user) {
    if (name && user.name !== name) {
      await supabaseAdmin
        .from('users')
        .update({ name, updated_at: new Date() })
        .eq('supabase_id', supabaseId);
    }
    return user;
  }
  
  const { data: newUser, error: createError } = await supabaseAdmin
    .from('users')
    .insert([{
      supabase_id: supabaseId,
      email: email,
      name: name || email.split('@')[0],
      plan: 'free',
      monthly_generations: 0,
      last_reset_date: new Date().toISOString()
    }])
    .select()
    .single();
  
  if (createError) {
    console.error('Error creating user:', createError);
    throw new Error('Failed to create user');
  }
  
  return newUser;
}

// ========== HELPER: Check and Reset Monthly Limit ==========
async function checkAndResetLimit(user) {
  const now = new Date();
  const lastReset = new Date(user.last_reset_date);
  
  if (now.getMonth() !== lastReset.getMonth() || now.getFullYear() !== lastReset.getFullYear()) {
    const { error } = await supabaseAdmin
      .from('users')
      .update({
        monthly_generations: 0,
        last_reset_date: now.toISOString()
      })
      .eq('id', user.id);
    
    if (!error) {
      user.monthly_generations = 0;
      user.last_reset_date = now.toISOString();
    }
  }
  return user;
}

// ========== HELPER: Check if User Can Generate ==========
async function canGenerate(supabaseId) {
  const { data: user, error } = await supabaseAdmin
    .from('users')
    .select('*')
    .eq('supabase_id', supabaseId)
    .single();
  
  if (error || !user) {
    return { allowed: false, message: 'User not found', user: null };
  }
  
  await checkAndResetLimit(user);
  
  if (user.plan === 'pro' || user.plan === 'lifetime') {
    return { allowed: true, user };
  }
  
  if (user.monthly_generations >= 500) {
    return { 
      allowed: false, 
      message: '❌ Free limit reached (500/month). Please upgrade to Pro for unlimited access.',
      user 
    };
  }
  
  return { allowed: true, user };
}

