// MAIN GENERATION ENDPOINT
app.post('/api/generate', async (req, res) => {
  const startTime = Date.now();
  
  try {
    const { supabaseId, prompt, platform } = req.body;
    
    if (!supabaseId) {
      return res.status(401).json({ error: 'Please login first' });
    }
    
    if (!prompt || prompt.trim() === '') {
      return res.status(400).json({ error: 'Please enter a prompt' });
    }
    
    if (!platform) {
      return res.status(400).json({ error: 'Please select a platform' });
    }
    
    console.log(`📥 Generation request: User=${supabaseId}, Platform=${platform}, Prompt="${prompt.substring(0, 50)}..."`);
    
    const { allowed, message, user } = await canGenerate(supabaseId);
    if (!allowed) {
      return res.status(403).json({ error: message });
    }
    
    let output = null;
    let usedFallback = false;
    
    if (OPENROUTER_API_KEY && OPENROUTER_API_KEY !== '') {
      console.log('🤖 Calling OpenRouter with Gemini Free Model...');
      output = await generateWithOpenRouter(prompt, platform);
      
      if (output) {
        console.log('✅ Gemini success');
      } else {
        console.log('⚠️ Gemini failed, using fallback');
        output = generateFallbackOutput(prompt, platform);
        usedFallback = true;
      }
    } else {
      console.log('⚠️ No API key, using fallback mode');
      output = generateFallbackOutput(prompt, platform);
      usedFallback = true;
    }
    
    const { data: userData } = await supabaseAdmin
      .from('users')
      .select('id')
      .eq('supabase_id', supabaseId)
      .single();
    
    if (userData) {
      await supabaseAdmin
        .from('generation_history')
        .insert([{
          user_id: userData.id,
          prompt: prompt,
          platform: platform,
          output: output.substring(0, 10000),
          output_type: platform.includes('Excel') || platform.includes('Sheets') ? 'tabular' : 'doc'
        }]);
      
      if (user && user.plan === 'free') {
        await supabaseAdmin
          .from('users')
          .update({ monthly_generations: (user.monthly_generations || 0) + 1 })
          .eq('supabase_id', supabaseId);
      }
    }
    
    const duration = Date.now() - startTime;
    console.log(`✅ Generation complete in ${duration}ms (${usedFallback ? 'fallback' : 'Gemini'})`);
    
    res.json({
      success: true,
      output: output,
      usedFallback: usedFallback,
      duration: duration
    });
    
  } catch (error) {
    console.error('❌ Generation error:', error);
    res.status(500).json({ error: 'Something went wrong. Please try again.' });
  }
});

app.get('/api/history/:supabaseId', async (req, res) => {
  try {
    const { supabaseId } = req.params;
    
    const { data: user, error: userError } = await supabaseAdmin
      .from('users')
      .select('id')
      .eq('supabase_id', supabaseId)
      .single();
    
    if (userError || !user) {
      return res.json([]);
    }
    
    const { data: history, error: historyError } = await supabaseAdmin
      .from('generation_history')
      .select('*')
      .eq('user_id', user.id)
      .order('created_at', { ascending: false })
      .limit(100);
    
    if (historyError) {
      console.error('Get history error:', historyError);
      return res.json([]);
    }
    
    res.json(history || []);
  } catch (error) {
    console.error('Get history error:', error);
    res.status(500).json({ error: 'Internal server error' });
  }
});

