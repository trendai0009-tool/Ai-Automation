// ========== HELPER: Generate using OpenRouter with Gemini Free Model ==========
async function generateWithOpenRouter(prompt, platform) {
  let systemPrompt = `You are Project Forge AI, an expert assistant that generates professional content for "${platform}".

RULES:
1. Generate content EXACTLY for ${platform} format
2. If Excel/Sheets: Return CSV format with headers and realistic data
3. If Word/Docs: Return professional document with headings and paragraphs
4. If PowerPoint/Slides: Return slide-by-slide outline with bullet points
5. If Tally/QuickBooks: Return accounting format with invoices/GST
6. Always include realistic numbers and categories
7. Keep response clear, structured, and professional
8. Hindi or English — jo user ne likha wahi use karo

OUTPUT FORMAT:
- Platform: ${platform}
- Clean, readable structure
- Ready to copy-paste or export`;

  try {
    const response = await fetch('https://openrouter.ai/api/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${OPENROUTER_API_KEY}`,
        'Content-Type': 'application/json',
        'HTTP-Referer': 'https://projectforgen.netlify.app',
        'X-Title': 'Project Forge AI'
      },
      body: JSON.stringify({
        model: 'google/gemini-2.0-flash-exp:free',
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: prompt }
        ],
        max_tokens: 2000,
        temperature: 0.7
      })
    });

    const data = await response.json();
    
    if (data.error) {
      console.error('OpenRouter API Error:', data.error);
      return null;
    }
    
    if (data.choices && data.choices[0] && data.choices[0].message) {
      return data.choices[0].message.content;
    }
    
    return null;
  } catch (error) {
    console.error('OpenRouter fetch error:', error.message);
    return null;
  }
}

